package random.Syntax;

public class Syntax {
    public static void main(String[] args) {
        int i = 11;
        long l = 400;
        char c = 'i';
        double d = 11.1;
        float f = 5.6F;
        boolean b = true;
        String s = "ivan";
        System.out.print(s.toUpperCase());
        System.out.println();
        System.out.println();
        System.out.printf("Integer is: %s\nLong is: %s\nFloat is: %s\nBoolean is: %s", i, l, f, b);
    }
}
